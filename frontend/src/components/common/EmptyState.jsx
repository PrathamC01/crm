import React from 'react';

const EmptyState = ({ 
  icon,
  title = "No Records Found",
  message = "No data available to display",
  actionButton = null,
  size = "normal"
}) => {
  const iconSize = size === "large" ? "h-16 w-16" : "h-12 w-12";
  const titleSize = size === "large" ? "text-xl" : "text-lg";

  const defaultIcon = (
    <svg className={`mx-auto ${iconSize} text-gray-400 mb-4`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1} d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
    </svg>
  );

  return (
    <div className="text-center py-12">
      <div className="text-gray-500">
        {icon || defaultIcon}
        <p className={`${titleSize} font-medium text-gray-900 mb-1`}>{title}</p>
        <p className="text-sm text-gray-500 mb-4">{message}</p>
        {actionButton && (
          <div className="mt-6">
            {actionButton}
          </div>
        )}
      </div>
    </div>
  );
};

